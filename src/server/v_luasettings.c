/* Called at InitGame time */
void vrx_init_lua()
{
	gi.dprintf("LUA: Initializing settings...");

	State = luaL_newstate();

	if (State)
	{
		luaL_openlibs(State);
		// q2lib
		luaL_newlib(State, q2lib);
		lua_setglobal(State, "q2");
		// vrxlib
		luaL_newlib(State, vrxlib);
		lua_setglobal(State, "vrx");
		lua_atpanic(State, printLuaError);
	}
	else
	{
		gi.dprintf("Warning: Couldn't initialize lua\n");
		return;
	}

	Lua_RunScript("settings");
	Lua_RunScript("variables");

	Lua_LoadVariables(); // vrxcl 4.2

	gi.dprintf("done.\n");
}

void vrx_close_lua()
{
	if (State)
		lua_close(State);
}

void vrx_lua_run_map_settings(char *mapname)
{
	if (!State)
		return;

	Lua_RunScript("initgame");
	Lua_RunScript(mapname);
}

double vrx_lua_get_double(char *varname)
{
	double setting = -1;

	lua_getglobal(State, varname);

	if (lua_isnumber(State, -1))
	{
		setting = lua_tonumber(State, -1);
	}

	lua_pop(State, -1);
	return setting;
}


const char *vrx_lua_get_string(char *varname)
{
	lua_getglobal(State, varname);
	if (lua_isstring(State, -1))
		return lua_tostring(State, -1);
	return NULL;
}

double vrx_lua_get_variable(char *varname, double default_var)
{
	double Setting = default_var;
	lua_getglobal(State, varname);

	if (lua_isnumber(State, -1))
	{
		Setting = lua_tonumber(State, -1);
	}
	else
	{
		gi.dprintf("Warning: Variable %s not found. Defaulting to %f\n", varname, default_var);
	}

	lua_pop(State, -1);
	return Setting;
}

int vrx_lua_get_int(char *varname, double default_var)
{
	return (int)vrx_lua_get_variable(varname, default_var);
}

void Lua_AdminLua(edict_t *ent, char *command)
{
	printent = ent;
	CHECK_LUA_ERR_NRET(luaL_loadstring(State, command), gi.cprintf(ent, PRINT_HIGH, "Err: %s", reason));
	CHECK_LUA_ERR_NRET(lua_pcall(State, 0, 0, 0), gi.cprintf(ent, PRINT_HIGH, "Err: %s", reason));
	printent = NULL;
}

/* Table Iteration */
qboolean vrx_lua_start_table_iter(const char *tablename)
{
	lua_getglobal(State, tablename);

	if (lua_istable(State, -1))
	{
		lua_pushnil(State);
		return true;
	}

	return false;
}

int vrx_lua_iter_next_string(char **out)
{
	int retval = lua_next(State, -2);
	if (retval != 0)
	{
		*out = strdup(lua_tostring(State, -1));
		lua_pop(State, 1);
	}
	return retval;
}

